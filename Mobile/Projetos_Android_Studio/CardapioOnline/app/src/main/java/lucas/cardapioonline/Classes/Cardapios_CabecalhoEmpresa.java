package lucas.cardapioonline.Classes;

public class Cardapios_CabecalhoEmpresa {

    //Dados da Empresa
    private String Nome_Empresa;
    private String Telefone_Empresa;
    private String Endereco_Empresa;
    private String Horario_Funcionamento_Empresa;
    private String Url_Logotipo_Empresa;

    public String getNome_Empresa() {
        return Nome_Empresa;
    }

    public void setNome_Empresa(String nome_Empresa) {
        Nome_Empresa = nome_Empresa;
    }

    public String getTelefone_Empresa() {
        return Telefone_Empresa;
    }

    public void setTelefone_Empresa(String telefone_Empresa) {
        Telefone_Empresa = telefone_Empresa;
    }

    public String getEndereco_Empresa() {
        return Endereco_Empresa;
    }

    public void setEndereco_Empresa(String endereco_Empresa) {
        Endereco_Empresa = endereco_Empresa;
    }

    public String getHorario_Funcionamento_Empresa() {
        return Horario_Funcionamento_Empresa;
    }

    public void setHorario_Funcionamento_Empresa(String horario_Funcionamento_Empresa) {
        Horario_Funcionamento_Empresa = horario_Funcionamento_Empresa;
    }

    public String getUrl_Logotipo_Empresa() {
        return Url_Logotipo_Empresa;
    }

    public void setUrl_Logotipo_Empresa(String url_Logotipo_Empresa) {
        Url_Logotipo_Empresa = url_Logotipo_Empresa;
    }


}
